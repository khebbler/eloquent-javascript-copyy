function countBs(string) {
  // initializing counter
  let count = 0;

  // looping through string characters
  for (let i = 0; i < string.length; i++) {
    // if character is B
    if (string[i] === 'B') {
      // incremeneting count
      count++
    }
  }
  // returning updated count
  return count;
}

