function isEven(number) {
  // negative to positive
  if (number < 0) {
    number = -number
  }

  // base if even
  if (number === 0) {
    return true;
  }
  // base if odd
  if (number === 1) {
    return false;
  }
  // recursion
  return isEven(number - 2);


}

